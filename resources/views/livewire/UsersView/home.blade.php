    <script>
        let currentSlide = 0;
        const slides = document.querySelectorAll('.carousel-item');

        function showSlide(n) {
            if (n >= slides.length) {
                currentSlide = 0;
            } else if (n < 0) {
                currentSlide = slides.length - 1;
            } else {
                currentSlide = n;
            }

            slides.forEach((slide, index) => {
                if (index === currentSlide) {
                    slide.style.display = 'block';

                    slide.classList.add('animate__animated', 'animate__slideInRight'); // Add fadeIn animation
                } else {
                    slide.style.display = 'none';
                    slide.classList.remove('animate__animated',
                        'animate__slideInLeft'); // Remove animation from other slides
                }
            });
        }

        function nextSlide() {
            showSlide(currentSlide + 1);
        }

        function prevSlide() {
            showSlide(currentSlide - 1);
        }

        function autoSlide() {
            nextSlide();
        }

        setInterval(autoSlide, 10000);

        document.addEventListener('livewire:navigated', function() {
            setTimeout(() => {
                showSlide(currentSlide);
            }, 1);
        });
    </script>
    <script>
        //#region - start of - number counter animation
        const counterAnim = (qSelector, start = 0, end, duration = 1000) => {
            const target = document.querySelector(qSelector);
            let startTimestamp = null;
            const step = (timestamp) => {
                if (!startTimestamp) startTimestamp = timestamp;
                const progress = Math.min((timestamp - startTimestamp) / duration, 1);
                target.innerText = Math.floor(progress * (end - start) + start);
                if (progress < 1) {
                    window.requestAnimationFrame(step);
                }
            };
            window.requestAnimationFrame(step);
        };
        //#endregion - end of - number counter animation

        document.addEventListener("livewire:navigated", () => {
            counterAnim("#count1", 10, 12000, 1000, 200);
            counterAnim("#count2", 500, 2, 2500);
            counterAnim("#count3", 500, 50, 1500);
